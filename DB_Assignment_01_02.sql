CREATE TABLE DEPARTMENTLSM (
	DEPTNO INT NOT NULL,
    DEPTNAME CHAR(10),
    FLOOR INT,
    PRIMARY KEY(DEPTNO)
);

CREATE TABLE EMPLOYEELSM (
	EMPNO INT NOT NULL,
    EMPNAME CHAR(10),
    TITLE CHAR(10),
    MANAGER INT,
    SALARY INT,
    HIREDATE DATE,
    DNO INT,
    PRIMARY KEY(EMPNO),
    FOREIGN KEY(MANAGER) REFERENCES EMPLOYEELSM(EMPNO),
    FOREIGN KEY(DNO) REFERENCES DEPARTMENTLSM(DEPTNO)
);

